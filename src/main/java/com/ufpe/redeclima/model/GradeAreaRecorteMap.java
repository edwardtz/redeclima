package com.ufpe.redeclima.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author edwardtz
 * Esta classe representa a relação entre uma N areas de recorte e N grades	
 */
@Entity
@Table(name="t_grade_area_recorte_map")
public class GradeAreaRecorteMap implements Serializable {

	private static final long serialVersionUID = 1L;
	
	@Id
	@Column(name="grade_id")
	private Long gradeId;
	
	@Id
	@Column(name="area_recorte_id")
	private Long areaRecorteId;

	public Long getGradeId() {
		return gradeId;
	}

	public void setGradeId(Long gradeId) {
		this.gradeId = gradeId;
	}

	public Long getAreaRecorteId() {
		return areaRecorteId;
	}

	public void setAreaRecorteId(Long areaRecorteId) {
		this.areaRecorteId = areaRecorteId;
	}
	
}
