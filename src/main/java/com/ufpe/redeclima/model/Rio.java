package com.ufpe.redeclima.model;

import java.io.Serializable;
import java.util.Collection;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.Table;

/**
 * @author edwardtz
 * Esta classe representa a entidade que armazena os dados dos rios
 */
@Entity
@Table(name="t_rio")
public class Rio implements Serializable {

	private static final long serialVersionUID = 1L;
	
	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	@Column(name="id_rio")
	private Long id;
	
	@Column(name="nome", nullable=false, unique=true)
	private String nome;
	
	@OneToMany(mappedBy="rio", targetEntity=Trecho.class)
    private Collection<Trecho> trechos;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Collection<Trecho> getTrechos() {
		return trechos;
	}

	public void setTrechos(Collection<Trecho> trechos) {
		this.trechos = trechos;
	}

}
